static void OLED_SendByte(uint8_t dat)
{
    while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET)
        ;
    SPI_I2S_SendData(SPI2, dat);
    while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET)
        ;
}

static void OLED_WriteCmd(uint8_t cmd)
{
    OLED_CS_CLR;
    OLED_DC_CLR;
    OLED_SendByte(cmd);
    OLED_CS_SET;
}

static void OLED_WriteData(uint8_t dat)
{
    OLED_CS_CLR;
    OLED_DC_SET;
    OLED_SendByte(dat);
    OLED_CS_SET;
}

/* ==========================================================
 * 4. CORE FUNCTIONS
 * ========================================================== */

void OLED_Refresh(void)
{
    uint8_t i, n;
    for (i = 0; i < 8; i++)
    {
        OLED_WriteCmd(0xb0 + i); // Set Page Address
        OLED_WriteCmd(0x00);     // Low Col
        OLED_WriteCmd(0x10);     // High Col
        for (n = 0; n < 128; n++)
        {
            OLED_WriteData(OLED_GRAM[i][n]);
        }
    }
}

void OLED_Clear(void)
{
    uint8_t i, n;
    for (i = 0; i < 8; i++)
    {
        for (n = 0; n < 128; n++)
        {
            OLED_GRAM[i][n] = 0x00;
        }
    }
}

void OLED_On(void)
{
    uint8_t i, n;
    for (i = 0; i < 8; i++)
    {
        for (n = 0; n < 128; n++)
        {
            OLED_GRAM[i][n] = 0x00;
        }
    }
}

void OLED_DisplayOn(void)
{
    OLED_WriteCmd(0x8D); // Charge Pump
    OLED_WriteCmd(0x14); // Enable
    OLED_WriteCmd(0xAF); // Display ON
}

void OLED_DisplayOff(void)
{
    OLED_WriteCmd(0x8D); // Charge Pump
    OLED_WriteCmd(0x10); // Disable
    OLED_WriteCmd(0xAE); // Display OFF
}

